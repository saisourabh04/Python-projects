# Write a Python program to calculate the Body Mass Index (BMI) given a person's weight in kilograms and height in meters.
# Rules for Calculating BMI:
# The BMI is calculated by dividing the weight by the square of the height.
# Based on the calculated BMI, determine the person's health status as follows:
# Underweight: BMI < 18.5
# Normal weight: 18.5 <= BMI < 25
# Overweight: 25 <= BMI < 30
# Obese: BMI >= 30
#
# Your program should:
# Ask the user for their weight (in kilograms).
# Ask the user for their height (in meters).
# Calculate the BMI based on the provided weight and height.
# Print the calculated BMI value and corresponding health status